ALTER PROCEDURE [dbo].[USP_GET_REPORTING_ESTATUS] -- USP_GET_REPORTING_ESTATUS 5,NULL
@IDMODULE INT,
@IROWFROM INT = NULL

AS
/* '===============================================================          
  '   NOMBRE                : 
  '   FECHA CREACIÆN        : 
  '   CREADO POR            : JUAN GUARECUCO
  '   CREADO PARA           : 
  '   FUNCIÆN               :  
  '   VERSIÆN               : 
  '   MODIFICADO EN         : 
  '   MODIFICADO POR        :  
  '   RAZÆN DE MODIFICACIÆN : 
  '===============================================================*/

SET XACT_ABORT ON               
SET NOCOUNT ON
SET LOCK_TIMEOUT 180000


DECLARE @TOPE INT

	IF @IROWFROM IS NULL
	BEGIN
		SET @TOPE=1000000
		SET @IROWFROM=0
	END
	ELSE
	BEGIN
		SET @TOPE=100
	END
	

BEGIN
     WITH T1 AS (
	SELECT ID,VDISPLAYESTATUS,IDMODULE FROM ACTION A WITH (NOLOCK)
	INNER JOIN ACTIONMODULE AM ON AM.IDACTION=A.ID
	WHERE AM.IDMODULE=@IDMODULE AND A.VDISPLAYESTATUS IS NOT NULL
	),
    T2 AS (SELECT COUNT(*) AS TOTAL FROM T1),
    T3 AS (
        SELECT * FROM T1
        ORDER BY T1.ID
        OFFSET @IROWFROM ROWS
        FETCH NEXT @TOPE ROWS ONLY
    )
    SELECT T3.*, T2.TOTAL
    FROM T3, T2

END
