SET XACT_ABORT ON;               
SET NOCOUNT ON;
SET LOCK_TIMEOUT 180000;

DECLARE @ISDEALER BIT=0
	IF NOT EXISTS (SELECT * FROM [dbo].[V_SUPPLIERS_USER] WHERE IDUSER=@IDUSER)
	BEGIN
	SET @ISDEALER=1
	END

	    DECLARE @IDMODULE INT=[dbo].[UFN_GET_IDMODULE]('VEHICULOS-VEHICULOS')
	

BEGIN


    WITH T1 AS (
        SELECT 
		M.IDSERVICETYPE,
		ST.VNAME AS  VSERVICETYPE,
		M.IDREPORTTYPE,
		RT.VNAME AS VREPORTTYPE,
		M.ID,
		M.IKM,
		M.IDVEHICLE,
		V.VVIN,
		M.IDDEALER,
		M.IESTATUS,
		A.VDISPLAYESTATUS AS VESTATUS,
		ISNULL(M.VSRGNUMBER,'') AS VSRGNUMBER,
		M.DMAINTENANCEDATE,
		M.NINVOICEAMOUNT,
		D.DEALER,
		D.VREFERENCEDEALER,
		M.DCREATED
        FROM MAINTENANCE M WITH (NOLOCK)
		INNER JOIN SERVICETYPE ST WITH(NOLOCK) ON M.IDSERVICETYPE=ST.ID
		INNER JOIN REPORTTYPE RT WITH (NOLOCK) ON M.IDREPORTTYPE=RT.ID
		INNER JOIN VEHICLE V WITH (NOLOCK) ON M.IDVEHICLE =V.ID
		INNER JOIN ACTION A WITH (NOLOCK) ON M.IESTATUS=A.ID
		INNER JOIN V_DEALERS D ON M.IDDEALER=D.IDDEALER
		WHERE 
		V.VVIN =@VVIN AND
		EXISTS (SELECT * FROM [dbo].[V_RELATEDS_USER] R 
						    WHERE R.IDUSER=@IDUSER 
							AND R.IDDEALER   =CASE WHEN @ISDEALER = 1 THEN V.IDDEALER ELSE R.IDDEALER END
							AND R.IDSUPPLIER =CASE WHEN @ISDEALER = 0 THEN V.IDSUPPLIER ELSE R.IDSUPPLIER END )
		AND V.IDSUPPLIER=@IDSUPPLIER


		UNION ALL
        
		SELECT
		M.IDSERVICETYPE,
		ST.VNAME AS  VSERVICETYPE,
		M.IDREPORTTYPE,
		RT.VNAME AS VREPORTTYPE,
		M.ID,
		M.KM AS IKM,
		V.ID AS IDVEHICLE,
		V.VVIN,
		M.IDDEALER,
		M.IESTATUS,
		A.VDISPLAYESTATUS AS VESTATUS,
		ISNULL(M.VSRGNUMBER,'') AS VSRGNUMBER,
		M.DMAINTENANCEDATE,
		M.NINVOICEAMOUNT,
		D.DEALER,
		D.VREFERENCEDEALER,
		M.DCREATED
        FROM RECORD M WITH (NOLOCK)
		INNER JOIN SERVICETYPE ST WITH(NOLOCK) ON M.IDSERVICETYPE=ST.ID
		INNER JOIN REPORTTYPE RT WITH (NOLOCK) ON M.IDREPORTTYPE=RT.ID
		INNER JOIN VEHICLE V WITH (NOLOCK) ON M.VVIN =V.VVIN
		INNER JOIN ACTION A WITH (NOLOCK) ON M.IESTATUS=A.ID
		INNER JOIN V_DEALERS D ON M.IDDEALER=D.IDDEALER
		WHERE 
		V.VVIN =@VVIN AND
		EXISTS (SELECT * FROM [dbo].[V_RELATEDS_USER] R 
						    WHERE R.IDUSER=@IDUSER 
							AND R.IDDEALER   =CASE WHEN @ISDEALER = 1 THEN V.IDDEALER ELSE R.IDDEALER END
							AND R.IDSUPPLIER =CASE WHEN @ISDEALER = 0 THEN V.IDSUPPLIER ELSE R.IDSUPPLIER END )
		AND V.IDSUPPLIER=@IDSUPPLIER
		

    ), 
    T2 AS (SELECT COUNT(*) AS TOTAL FROM T1)

    SELECT T1.*, T2.TOTAL
    FROM T1, T2
    WHERE T2.TOTAL > 0

END;

