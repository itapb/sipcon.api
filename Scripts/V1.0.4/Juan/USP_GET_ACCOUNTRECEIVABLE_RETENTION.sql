CREATE PROCEDURE [dbo].[USP_GET_ACCOUNTRECEIVABLE_RETENTION] -- USP_GET_ACCOUNTRECEIVABLE_RETENTION 'J500681054'
@VSUPPLIERVAT VARCHAR(12)
AS
/* '===============================================================          
  '   NOMBRE                : 
  '   FECHA CREACI”N        : 
  '   CREADO POR            : JUAN GUARECUCO
  '   CREADO PARA           : 
  '   FUNCI”N               :  
  '   VERSI”N               : 
  '   MODIFICADO EN         : 
  '   MODIFICADO POR        :  
  '   RAZ”N DE MODIFICACI”N : 
  '===============================================================*/

SET XACT_ABORT ON               
SET NOCOUNT ON
SET LOCK_TIMEOUT 180000

	DECLARE @TOPE INT

	DECLARE  @IDSUPPLIER INT

	SET @IDSUPPLIER= (SELECT IDSUPPLIER FROM V_SUPPLIERS WHERE VVAT=@VSUPPLIERVAT) 



BEGIN
	WITH 
		T1 AS (
			-- 3. FILTRAR MUESTRA DE PART
			SELECT 
			A.ID,
			D.DEALER AS VDEALER,
			D.VVATINVOICE AS VDEALERVAT,
			T.VCODE AS VTYPECODE,
			C.VCODE AS VCONCEPTCODE,
			A.VNUMBER,
			A.VREFERENCE,
			A.DDATE,
			A.DDUEDATE,
			A.NAMOUNT,
			A.NBALANCE,
			COUNT(*) OVER() AS TOTAL
			FROM dbo.ACCOUNTRECEIVABLE A WITH (NOLOCK)
			INNER JOIN SETTLEMENTS S WITH (NOLOCK) ON S.IDACCOUNTRECEIVABLE = A.ID
			INNER JOIN PAYMENT P WITH (NOLOCK) ON S.IDPAYMENT = P.ID
			INNER JOIN [dbo].[DOCUMENTTYPE] T WITH (NOLOCK) ON T.VCODE = A.VTYPE
			INNER JOIN [dbo].[DOCUMENTCONCEPT] C WITH (NOLOCK) ON C.VCODE = A.VCONCEPT
			INNER JOIN V_DEALERS D ON D.IDDEALER = A.IDDEALER
			WHERE A.IDSUPPLIER = @IDSUPPLIER
			AND P.BASINC=0
			AND P.VCONCEPT ='R'
			-- L”GICA: No debe existir ning˙n detalle que tenga un estatus diferente a 'APPROVE'
			AND NOT EXISTS (
				SELECT 1 
				FROM PAYMENTDETAILS PD WITH (NOLOCK) 
				WHERE PD.IDPAYMENT = P.ID 
				AND PD.IDSTATUS <> dbo.UFN_GET_ISTATUS('APPROVE')
			)
			-- OPCIONAL: Asegurar que al menos tenga un detalle (que no sea un pago vacÌo)
			AND EXISTS (
				SELECT 1 
				FROM PAYMENTDETAILS PD WITH (NOLOCK) 
				WHERE PD.IDPAYMENT = P.ID
			)
		)
		 -- 5. PAGINAR 
		SELECT T1.* 
		FROM T1
        ORDER BY T1.DDATE,T1.VNUMBER
END